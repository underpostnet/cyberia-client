import logging
import random
from enum import Enum, auto
import os
import json


# This file centralizes object layer data, enums, and related constants.
logging.basicConfig(
    level=logging.INFO, format="%(asctime)s - %(levelname)s - %(message)s"
)


class Direction(Enum):
    """Defines possible directions for animated objects."""

    UP = auto()
    UP_RIGHT = auto()
    RIGHT = auto()
    DOWN_RIGHT = auto()
    DOWN = auto()
    DOWN_LEFT = auto()
    LEFT = auto()
    UP_LEFT = auto()
    NONE = auto()  # For objects without a specific direction (e.g., static)


class ObjectLayerMode(Enum):
    """Defines animation modes (e.g., idle, walking)."""

    IDLE = auto()
    WALKING = auto()


def _generate_seed_data() -> dict:
    """Generates random integer values between 0 and 10 for seed data attributes."""
    return {
        "EFFECT": random.randint(0, 10),
        "RESISTANCE": random.randint(0, 10),
        "AGILITY": random.randint(0, 10),
        "RANGE": random.randint(0, 10),
        "INTELLIGENCE": random.randint(0, 10),
        "UTILITY": random.randint(0, 10),
    }


OBJECT_LAYER_DATA = {}


for dirpath, dirnames, filenames in os.walk("./object_layer"):
    for filename in filenames:
        if filename.startswith("object_layer_") and ".json" in filename:
            with open(dirpath + "/" + filename) as json_file:
                data = json.load(json_file)
                object_layer_id = (
                    filename.replace(".json", "")
                    .replace("object_layer_data_", "")
                    .upper()
                )
                print("Load object layer data", object_layer_id)
                OBJECT_LAYER_DATA[object_layer_id] = data


